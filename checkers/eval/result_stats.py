import json
from pathlib import Path

def analyze_result_file(file_path: Path):
    print(f"\n========================================\nAnalyzing: {file_path.name}")
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            data = json.load(f)
    except Exception as e:
        print(f"Error loading {file_path.name}: {e}")
        return

    # Check structure
    meta = data.get("meta", {})
    summary = data.get("summary", {})
    results = data.get("results", [])
    
    if not results:
        # Check if this has a different structure
        if isinstance(data, list):
            results = data
        else:
            print("No results list found.")
            return
            
    print(f"Total positions evaluated: {len(results)}")
    
    # Analyze results
    total_valid = 0
    engine_covered = 0
    kr_covered = 0
    has_kr = 0
    
    total_proposed_count = 0
    total_legal_count = 0
    total_valid_proposed = 0
    total_illegal_proposed = 0
    
    perfect_count = 0
    illegal_geometry_count = 0
    out_of_bounds_count = 0
    duplicate_moves_count = 0
    partial_jumps_count = 0
    missing_legal_moves_count = 0
    
    for r in results:
        # Check if it has best_move_covered or contains_engine_best
        # Some result files might have been generated by proposal_seperation_eval.py
        # and some by another pipeline. Let's handle both.
        contains_engine = r.get("contains_engine_best")
        contains_kr = r.get("contains_kingsrow_best")
        
        # fallback to older format keys if present
        if contains_engine is None:
            contains_engine = r.get("best_move_covered")
        if contains_kr is None:
            contains_kr = r.get("near_best_covered")
            
        legal_count = r.get("legal_move_count")
        
        # proposed moves
        proposed_paths = r.get("proposed_paths")
        if proposed_paths is not None:
            proposed_count = len(proposed_paths)
        else:
            # proposal_seperation_eval.py format
            prop_class = r.get("proposal_classification", {})
            proposed_count = prop_class.get("proposed_count", 0)
            
        # Parse taxonomy
        tax = r.get("failure_taxonomy", {})
        duplicate_moves_count += tax.get("duplicate_moves_generated", 0)
        partial_jumps_count += tax.get("partial_jump_sequences", 0)
        illegal_geometry_count += tax.get("illegal_geometry_moves", 0)
        out_of_bounds_count += tax.get("out_of_bounds_coordinates", 0)
        
        classification = r.get("classification", r.get("proposal_classification", {}).get("classification", "unknown"))
        if classification == "perfect":
            perfect_count += 1
            
        total_legal_count += legal_count if legal_count is not None else 0
        total_proposed_count += proposed_count
        
        if contains_engine is not None:
            total_valid += 1
            if contains_engine is True:
                engine_covered += 1
                
        if contains_kr is not None:
            has_kr += 1
            if contains_kr is True:
                kr_covered += 1

    if total_valid > 0:
        print(f"Engine best-move coverage: {engine_covered} / {total_valid} ({engine_covered/total_valid*100:.1f}%)")
    if has_kr > 0:
        print(f"KingsRow best-move coverage: {kr_covered} / {has_kr} ({kr_covered/has_kr*100:.1f}%)")
        
    print(f"Average legal moves per position: {total_legal_count/len(results):.2f}" if len(results) > 0 else "")
    print(f"Average proposed moves per position: {total_proposed_count/len(results):.2f}" if len(results) > 0 else "")
    print(f"Perfect proposals rate: {perfect_count} / {len(results)} ({perfect_count/len(results)*100:.1f}%)" if len(results) > 0 else "")
    print(f"Duplicates: {duplicate_moves_count}, Partial jumps: {partial_jumps_count}, Illegal geometry: {illegal_geometry_count}, Out of bounds: {out_of_bounds_count}")

def main():
    logs_dir = Path(__file__).parent.parent.parent / "logs"
    for f in logs_dir.glob("*.json"):
        if "proposal_coverage" in f.name or "proposal_seperation" in f.name:
            analyze_result_file(f)

if __name__ == "__main__":
    main()
